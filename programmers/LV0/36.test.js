const solution = require("./36.순서쌍의개수");

describe("36. 순서쌍의개수", () => {
  it(`(1) n이 20 이므로 곱이 20인 순서쌍은 (1, 20), (2, 10), (4, 5), (5, 4), (10, 2), (20, 1) 이므로 6을 return합니다.`, () => {
    expect(solution(20)).toBe(6);
  });
  it(`(2) n이 100 이므로 곱이 100인 순서쌍은 (1, 100), (2, 50), (4, 25), (5, 20), (10, 10), (20, 5), (25, 4), (50, 2), (100, 1) 이므로 9를 return합니다.`, () => {
    expect(solution(100)).toBe(9);
  });
});
